public class book
{
String code;
String title;
double price;
public book(String book_code,String book_title,double book_price)
{
    code=book_code;
    title=book_title;
    price=book_price;
}
public String getCode()
{
    return code;
}
public String getTitle()
{
    return title;
}
public double getPrice()
{
    return price;
}
}
    